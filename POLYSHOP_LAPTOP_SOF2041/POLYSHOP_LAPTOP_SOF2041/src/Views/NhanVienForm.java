    private void showData(List<NhanVien> lists) {
        int i = 1;
        tblmodel.setRowCount(0);
        for (NhanVien nv : lists) {
            Object[] data = new Object[]{
                i++, nv.getMa(), nv.getTen(), nv.getGioiTinh(), nv.getNgaySinh(), nv.getDiaChi(),
                nv.getSdt(), nv.getTenCH(), nv.getTenCV(), nv.getTrangThai() == 0 ? "Làm" : "Nghỉ"
            };
            tblmodel.addRow(data);
        }
    }

    private void fillData(int index) {
        NhanVien nv = listNV.get(index);
        txtMa.setText(nv.getMa());
        txtHo.setText(nv.getHo());
        txtTenDem.setText(nv.getTenDem());
        txtTen.setText(nv.getTen());
        txtSdt.setText(nv.getSdt());
        txtNgaySinh.setText(String.valueOf(nv.getNgaySinh()));
        txtDiaChi.setText(nv.getDiaChi());
        int trangThai = nv.getTrangThai();
        String tt = null;
        if (trangThai == 0) {
            tt = "Làm";
        } else {
            tt = "Nghỉ";
        }
        cbbTrangThai.setSelectedItem(tt);
        cbbChucVu.setSelectedItem(nv.getTenCV());
        cbbCuaHang.setSelectedItem(nv.getTenCH());
        if (nv.getGioiTinh().contains("Nam")) {
            rbNam.setSelected(true);
        } else {
            rbNu.setSelected(true);
        }

    }

    private void loadCbb() {
        for (CuaHang ch : listCH) {
            cbbCuaHangModel.addElement(ch.getTen());
        }

        for (ChucVu cv : listCV) {
            cbbChucVuModel.addElement(cv.getTen());
        }
    }

