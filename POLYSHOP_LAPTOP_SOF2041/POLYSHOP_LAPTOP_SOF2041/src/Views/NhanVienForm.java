    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        String tenDem = txtTenDem.getText();
        String ho = txtHo.getText();
        String ngaySinh = txtNgaySinh.getText();
        String sdt = txtSdt.getText();
        String diaChi = txtDiaChi.getText();
        int index = cbbCuaHang.getSelectedIndex();
        CuaHang ch = listCH.get(index);
        int index2 = cbbChucVu.getSelectedIndex();
        ChucVu cv = listCV.get(index2);
        String trangThai = (String) cbbTrangThai.getSelectedItem();
        int tt;
        if (trangThai.contains("Làm")) {
            tt = 0;
        } else {
            tt = 1;
        }
        boolean GioiTinh = rbNam.isSelected();
        String gt;
        if (GioiTinh == true) {
            gt = "Nam";
        } else {
            gt = "Nữ";
        }
        boolean trung = false;
        for (NhanVien nv : listNV) {
            if (nv.getMa().contains(ma)) {
                trung = true;
            }
        }
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
        } else if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
        } else if (tenDem.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên đệm không được để trống");
        } else if (ho.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Họ không được để trống");
        } else if (ngaySinh.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ngày sinh không được để trống");
        } else if (sdt.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số điện thoại không được để trống");
        } else if (diaChi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên địa chỉ không được để trống");
        } else if (trung) {
            JOptionPane.showMessageDialog(this, "Mã không được để trùng");
        } else {

            NhanVien nv = new NhanVien(ma, ten, tenDem, ho, gt, Date.valueOf(ngaySinh), diaChi, sdt, ch.getIdCH(), cv.getIdCV(), tt);
            JOptionPane.showMessageDialog(this, nvs.add(nv));
            listNV = nvs.getAll();
            showData(listNV);
        }
    }//GEN-LAST:event_btnThemActionPerformed

