    private void showData(List<HoaDonChiTiet> lists) {
        int i = 1;
        tblmodel.setRowCount(0);
        for (HoaDonChiTiet hdct : lists) {
            Object[] data = new Object[]{
                i++, hdct.getIdHoaDon(), hdct.getIdCTSanPham(), hdct.getSoLuong(), hdct.getDonGia()
            };
            tblmodel.addRow(data);
        }

    }

    private void fillData(int index) {
        HoaDonChiTiet hdct = listHDCT.get(index);
        cbbCTSP.setSelectedItem(hdct.getIdCTSanPham());
        cbbMaHD.setSelectedItem(hdct.getIdHoaDon());
        txtSoLuong.setText(String.valueOf(hdct.getSoLuong()));
        txtDonGia.setText(String.valueOf(hdct.getDonGia()));
    }

    private void addCbb() {
        for (HoaDon hoaDon : listHD) {
            cbbHDModel.addElement(hoaDon.getMa());
        }
        for (ChiTietSanPham ctsp : listCTSP) {
            cbbCTSPModel.addElement(ctsp.getIdCTSP());
        }
    }

