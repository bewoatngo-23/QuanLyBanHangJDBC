    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String soLuong = txtSoLuong.getText();
        String donGia = txtDonGia.getText();
//        String maHD = cbbMaHD.getSelectedItem().toString();
//        HoaDon hd = hds.getOne(maHD);
//        String idCTSP = cbbCTSP.getSelectedItem().toString();
//        ChiTietSanPham ctsp = ctsps.getOne(idCTSP);
        int index = cbbMaHD.getSelectedIndex();
        HoaDon hd = listHD.get(index);
        int index2 = cbbCTSP.getSelectedIndex();
        ChiTietSanPham ctsp = listCTSP.get(index2);

        if (soLuong.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số lượng không được để trống");
        } else if (donGia.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Đơn giá không được để trống");
        } else if (soLuong.matches("\\d+") == false) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số");
        } else if (donGia.matches("\\d+") == false) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải là số");
        } else {
            HoaDonChiTiet hdct = new HoaDonChiTiet(hd.getIdHD(), ctsp.getIdCTSP(), Integer.valueOf(soLuong), Double.valueOf(donGia));
            JOptionPane.showMessageDialog(this, hdctService.add(hdct));
            listHDCT = hdctService.getAllHDCT();
            showData(listHDCT);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String soLuong = txtSoLuong.getText();
        String donGia = txtDonGia.getText();
//        String maHD = cbbMaHD.getSelectedItem().toString();
//        HoaDon hd = hds.getOne(maHD);
        String id = cbbCTSP.getSelectedItem().toString();
        if (soLuong.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Số lượng không được để trống");
        } else if (donGia.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Đơn giá không được để trống");
        } else if (soLuong.matches("\\d+") == false) {
            JOptionPane.showMessageDialog(this, "Số lượng phải là số");
        } else if (donGia.matches("\\d+") == false) {
            JOptionPane.showMessageDialog(this, "Đơn giá phải là số");
        } else {
            HoaDonChiTiet hdct = new HoaDonChiTiet(id, Integer.valueOf(soLuong), Double.valueOf(donGia));
            JOptionPane.showMessageDialog(this, hdctService.update(id, hdct));
            listHDCT = hdctService.getAllHDCT();
            showData(listHDCT);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

