    private void showData(List<Nsx> lists) {
        tblmodel.setRowCount(0);
        int i = 1;
        for (Nsx nsx : lists) {
            Object[] data = new Object[]{
                i++, nsx.getIdNsx(), nsx.getMa(), nsx.getTen()
            };
            tblmodel.addRow(data);
        }
    }

    private void fillData(int index) {
        Nsx nsx = listNsx.get(index);
        txtMaNsx.setText(nsx.getMa());
        txtTen.setText(nsx.getTen());
    }

