    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String ma = txtMaNsx.getText();
        String ten = txtTen.getText();
        boolean trung = false;
        for (Nsx n : listNsx) {
            if (n.getMa().contains(ma)) {
                trung = true;
            }
        }
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
        } else if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
        } else if (trung) {
            JOptionPane.showMessageDialog(this, "Mã không được để trùng");
        } else {
            Nsx nsx = new Nsx(ma, ten);
            JOptionPane.showMessageDialog(this, nsxService.add(nsx));
            listNsx = nsxService.getAll();
            showData(listNsx);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String ma = txtMaNsx.getText();
        String ten = txtTen.getText();
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã không được để trống");
        } else if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên không được để trống");
        } else {
            Nsx nsx = new Nsx(ma, ten);
            JOptionPane.showMessageDialog(this, nsxService.update(ma, nsx));
            listNsx = nsxService.getAll();
            showData(listNsx);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String ma = txtMaNsx.getText();
        JOptionPane.showMessageDialog(this, nsxService.delete(ma));
        listNsx = nsxService.getAll();
        showData(listNsx);
    }//GEN-LAST:event_btnXoaActionPerformed

